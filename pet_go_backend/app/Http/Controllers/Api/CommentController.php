    {
        //

        $id = $request['userData']->id;
        $comment = DB::select('SELECT a.name , a.img , b.user_id , b.comment , b.updated_at
        from pets as a
        INNER JOIN experiences as b on a.id = b.pet_id
        WHERE b.user_id = ?', [$id]);

        if ($comment == NULL) {
            return response()->json([
                'status' => '暫無評論',
            ]);
        } else {
            return response()->json([
                'status' => '讀取成功',
                'comment' => $comment,
            ]);
        }
    }

